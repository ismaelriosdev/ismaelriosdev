# Função para listar os alimentos
def listar_alimentos():
    print("Lista de Alimentos:")
    for alimento in alimentos.keys():
        print(alimento)

# Função para pesquisar um alimento
def pesquisar_alimento():
    while True:
        print("\n--- Menu ---")
        print("1. Pesquisar Alimento")
        print("2. Listar Alimentos")
        print("3. Editar Alimento")
        print("4. Adicionar Alimento")
        print("5. Sair")
        escolha = input("Escolha uma opção (1, 2, 3, 4 ou 5): ")
        
        if escolha == '1':
            nome_alimento = input("Digite o nome do alimento (ou 'sair' para encerrar): ")
            if nome_alimento.lower() == "sair":
                break
            if nome_alimento in alimentos:
                print(f"Nome do alimento: {nome_alimento}")
                print(f"Calorias: {alimentos[nome_alimento]['calorias']}")
                print(f"Descrição: {alimentos[nome_alimento]['descricao']}")
                print(f"Pontuação Nutricional: {alimentos[nome_alimento]['pontuacao_nutricional']}")
            else:
                print("Alimento não encontrado. Tente novamente.")
        
        elif escolha == '2':
            listar_alimentos()
        
        elif escolha == '3':
            editar_alimento()

        elif escolha == '4':
            adicionar_alimento()
        
        elif escolha == '5':
            break
        
        else:
            print("Opção inválida. Tente novamente.")

# Função para editar informações de um alimento
def editar_alimento():
    nome_alimento = input("Digite o nome do alimento que deseja editar: ")
    if nome_alimento in alimentos:
        print(f"Editando informações de {nome_alimento}.")
        # Solicite ao usuário para fornecer as novas informações (calorias, descrição, pontuação nutricional)
        calorias = input("Digite as novas calorias (ou pressione Enter para manter a mesma): ")
        descricao = input("Digite a nova descrição (ou pressione Enter para manter a mesma): ")
        pontuacao = input("Digite a nova pontuação nutricional (ou pressione Enter para manter a mesma): ")

        # Atualize as informações (se o usuário digitou algo)
        if calorias:
            alimentos[nome_alimento]['calorias'] = calorias
        if descricao:
            alimentos[nome_alimento]['descricao'] = descricao
        if pontuacao:
            alimentos[nome_alimento]['pontuacao_nutricional'] = int(pontuacao)

        print(f"As informações de {nome_alimento} foram atualizadas.")
    else:
        print("Alimento não encontrado. Tente novamente.")

# Função para adicionar um novo alimento
def adicionar_alimento():
    nome_alimento = input("Digite o nome do novo alimento: ")
    if nome_alimento not in alimentos:
        calorias = input("Digite as calorias: ")
        descricao = input("Digite a descrição: ")
        pontuacao = int(input("Digite a pontuação nutricional: "))

        # Crie um novo dicionário para o novo alimento
        novo_alimento = {
            "calorias": calorias,
            "descricao": descricao,
            "pontuacao_nutricional": pontuacao
        }

        # Adicione o novo alimento ao dicionário de alimentos
        alimentos[nome_alimento] = novo_alimento

        print(f"{nome_alimento} foi adicionado à lista de alimentos.")
    else:
        print("O alimento já existe na lista. Tente novamente com um nome diferente.")
# Chamando a função de pesquisa
